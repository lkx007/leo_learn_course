import os

def speak(text):
    """同时在屏幕打印文字并语音播报"""
    print(text)
    # 语音播报（过滤掉表情符号，语速设为匀速）
    clean_text = text.replace("-", "").replace("\n", "").strip()
    if clean_text:
        os.system(f"say -r 150 '{clean_text}'")

# --------------------------
# 例子 1：装数字
# --------------------------
speak("--- 例子 1: 年龄 ---")
age = 7
speak("我的年龄是:")
speak(str(age))

# --------------------------
# 例子 2：装名字
# --------------------------
speak("\n--- 例子 2: 名字 ---")
name = "Ultraman"  # 奥特曼
speak("我的名字是:")
speak(name)

